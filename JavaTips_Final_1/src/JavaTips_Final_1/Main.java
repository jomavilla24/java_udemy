/* ======================== Final Classes and Methods =========================
 * A class is final if we use the "final" keyword in the definition of that class
 * A Final class can't be extended. This is useful when you don't want any other
 * class to be able to modify the behavior of that class.
 * 
 * A final method is a method that uses the "final" keyword in its definition.
 * No subclass can overwrite a final method. This is useful when you want to be 
 * able to extend a class, but you don't want any of those subclasses to be able
 * to change the behavior of a particular method.
 * */
